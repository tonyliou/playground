tuple = ('Thomson', 78, 12.58, 'Sunny', 180.2)
tinytuple = (123, 'Tony')
print("tuple:", tuple)  # 輸出完整元組
print("tinytuple:", tinytuple)  # 輸出完整元組
print("tuple[0]:", tuple[0])  # 輸出元組的第一個元素
print("tuple[1:3]:", tuple[1:3])  # 輸出第二個至第三個元素
print("tuple[2:]:", tuple[2:])  # 輸出從第三個開始至清單末尾的所有元素
print("tinytuple * 2:", tinytuple * 2)  # 輸出元組兩次
print("tuple + tinytuple:", tuple + tinytuple)  # 列印組合的元組
# tuple[1] = 100 # 不能修改元組內的元素
