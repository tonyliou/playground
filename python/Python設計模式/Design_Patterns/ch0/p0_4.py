dict = {}
dict['one'] = "This is one"
dict[2] = "This is two"
tinydict = {'name': 'Tony', 'age': 24, 'height': 177}

print("tinydict:", tinydict) # 輸出完整的字典
print("tinydict.keys():", tinydict.keys())  # 輸出所有鍵
print("tinydict.values():", tinydict.values())  # 輸出所有值
print("dict['one']:", dict['one'])  # 輸出鍵為'one' 的值
print("dict[2]:", dict[2])  # 輸出鍵為 2 的值
